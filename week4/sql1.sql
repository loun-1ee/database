-- ============================================
-- 1. 테이블 생성 (CREATE TABLE)
-- ============================================

-- 제품 테이블
CREATE TABLE 제품 (
    제품번호   CHAR(3)      NOT NULL,
    제품명     VARCHAR(20),
    재고량     INT,
    단가       INT          DEFAULT 0,
    제조업체   VARCHAR(20),
    PRIMARY KEY (제품번호)
);

-- 고객 테이블
CREATE TABLE 고객 (
    고객아이디  VARCHAR(20)  NOT NULL,
    고객이름    VARCHAR(10),
    나이        INT,
    등급        VARCHAR(10),
    직업        VARCHAR(20),
    적립금      INT          DEFAULT 0,
    PRIMARY KEY (고객아이디)
);


