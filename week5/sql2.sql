-- 1. 등급이 vip이거나 적립금이 3000원 이상인 고객 검색
SELECT 고객이름, 등급, 직업, 적립금
FROM 고객
WHERE 등급 = 'vip' OR 적립금 >= 3000;

-- 2. 고객아이디가 5자인 고객 검색
SELECT 고객아이디, 고객이름, 등급
FROM 고객
WHERE 고객아이디 LIKE '_____';

-- 3. 적립금이 1500원 이상인 고객을 고객아이디 기준 오름차순 정렬
SELECT 고객아이디, 고객이름, 등급, 적립금
FROM 고객
WHERE 적립금 >= 1500
ORDER BY 고객아이디 ASC;

-- 4. 고객 테이블에 핸드폰번호 속성 추가 (기본값: 010-0000-0000)
ALTER TABLE 고객
ADD 핸드폰번호 VARCHAR(13) DEFAULT '010-0000-0000';

-- 5. 고객 테이블에서 가입날짜 속성 삭제 (참조 존재 시 삭제 거부)
ALTER TABLE 고객
DROP 가입날짜 RESTRICT;
